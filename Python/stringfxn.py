text = "   Sandhyarani Torase    "

# print original string
print("Original:", text)

# print string length
print("Length:", len(text))

# print string lowercase
print("Lowercase:", text.lower())

# print string uppercase
print("Uppercase:", text.upper())

# remove space in string
print("Stripped:", text.strip())

# print replaced string
print("Replaced:", text.replace("Sandhyarani", "Hrithik"))

#  .startswith() function in string
print("Starts with 'Hello':", text.strip().startswith("Hello"))

#  .split() function in string
print("Split:", text.strip().split())


# Join with space
print("Joined:","-".join(text))

