# Define a function that takes no arguments and returns nothing
def my_function():
    print("Hello, world!")

# Define a function with parameters
def add_numbers(a, b):
    return a + b

# Define a function with a default parameter
def greet_person(name="Sandhyarani"):
    print(f"Hello, {name}!")

# Call the functions
my_function()  

result = add_numbers(5, 3)
print("Sum:", result)  

greet_person("Alice")  
greet_person()         
